/**
 * This interface holds information regarding directions of sprites.
 */
public interface Direction {

    // Define directions
    /** This represents no direction */
    public static final int DIR_NONE = 0;
    /** This represents the left direction */
    public static final int DIR_LEFT = 1;
    /** This represents the right direction */
    public static final int DIR_RIGHT = 2;
    /** This represents the up direction */
    public static final int DIR_UP = 3;
    /** This represents the down direction */
    public static final int DIR_DOWN = 4;
}
